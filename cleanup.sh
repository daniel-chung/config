#!/usr/bin/env zsh

# -----------------------------------------------------------------------------
# Alacritty
# -----------------------------------------------------------------------------
cleanup_alacritty () {
	rm ~/.alacritty.toml
}

# -----------------------------------------------------------------------------
# Prezto
# -----------------------------------------------------------------------------
cleanup_prezto () {
	setopt EXTENDED_GLOB
	for rcfile in "${ZDOTDIR:-$HOME}"/.config/prezto/runcoms/^README.md(.N); do
		rm "${ZDOTDIR:-$HOME}/.${rcfile:t}"
	done
}

# -----------------------------------------------------------------------------
# Setup
# -----------------------------------------------------------------------------
cleanup_alacritty
cleanup_prezto
